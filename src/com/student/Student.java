package com.student;


public class Student {

	private int id;
	private String name;
	private String boy;
	private String gender;
	private String age;
	private String grade;
	private String email;
	private String contact;
	private String address;
	private String gname;
	private String gtype;
	private String gemail;
	private String gaddress;
	private String gcontact;
	private String occupation;
	private String userName;
	private String password;
	
	public Student(int id, String name, String boy, String gender, String age, String grade, String email, String contact,
			String address, String gname, String gtype, String gemail, String gaddress, String gcontact,
			String occupation, String userName, String password) {
		super();
		this.id = id;
		this.name = name;
		this.boy = boy;
		this.gender = gender;
		this.age = age;
		this.grade = grade;
		this.email = email;
		this.contact = contact;
		this.address = address;
		this.gname = gname;
		this.gtype = gtype;
		this.gemail = gemail;
		this.gaddress = gaddress;
		this.gcontact = gcontact;
		this.occupation = occupation;
		this.userName = userName;
		this.password = password;
	}

	public int getId() {
		return id;
	}

	

	public String getName() {
		return name;
	}

	

	public String getBoy() {
		return boy;
	}

	

	public String getGender() {
		return gender;
	}

	

	public String getAge() {
		return age;
	}

	

	public String getGrade() {
		return grade;
	}

	

	public String getEmail() {
		return email;
	}

	

	public String getContact() {
		return contact;
	}


	public String getAddress() {
		return address;
	}

	

	public String getGname() {
		return gname;
	}

	
	public String getGtype() {
		return gtype;
	}

	
	public String getGemail() {
		return gemail;
	}

	

	public String getGaddress() {
		return gaddress;
	}

	

	public String getGcontact() {
		return gcontact;
	}

	

	public String getOccupation() {
		return occupation;
	}

	

	public String getUserName() {
		return userName;
	}

	

	public String getPassword() {
		return password;
	}

	
	
	
	
}
